# Example
our_name = "Free Code Camp"
our_str = "Hello, our name is " <> our_name <> ", how are you?"

# Only change code below this line
my_name = "Chase Pursley"
my_str = "My name is " <> my_name <> " and I am swell!"
# => "My name is Chase Pursley and I am swell!"

# or using string interpolation
my_str_2 = "My name is #{my_name} and I am swell!"
# => "My name is Chase Pursley and I am swell!"
