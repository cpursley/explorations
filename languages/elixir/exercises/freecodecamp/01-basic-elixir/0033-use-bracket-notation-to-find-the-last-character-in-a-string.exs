# Note that Elixir does not use bracket notation for string access
# Instead, use String.at("my_string", -1) to find character by index

# Example
first_name = "Ada"
last_letter_of_first_name = String.at(first_name, -1)

# Setup
last_name = "Lovelace"

# Only change code below this line.
last_letter_of_last_name = String.at(last_name, -1)
# => "e"
