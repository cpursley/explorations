word_blanks =
  fn(my_noun, my_adjective, my_verb, my_adverb) ->
    "My #{my_adjective} #{my_noun} #{my_verb} #{my_adverb}."
  end

# Change the words here to test your function
word_blanks.("dog", "big", "ran", "quickly")
# => "My big dog ran quickly."
