# In Elixir, use List.replace_at([1, 2, 3], 0, 0) to replace a List item by index

# Example
our_list = [1, 2, 3]
List.replace_at(our_list, 1, 3)
# => [1, 3, 3]

# Setup
my_list = [1, 2, 3]

# Only change code below this line.

